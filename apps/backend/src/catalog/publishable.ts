/**
 * Гейт публикации на витрину (ТЗ 8.2): показываются только товары, привязанные
 * к категории. Товар без категории ломал витрину незаметно: попадал в выдачу и
 * в «Найдено N», но был недостижим фильтром категорий (categoryCounts считает
 * только привязанные) — «Найдено 14», а сумма счётчиков 8. Скрываем до
 * категоризации контент-менеджером; после привязки в Strapi товар появляется
 * сам (кеш каталога 60с).
 */
export function hasStorefrontCategory(sp: {
  category?: { slug?: string | null } | null;
}): boolean {
  return Boolean(sp.category?.slug);
}
